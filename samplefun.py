
def add(a, b):
    return (a + b)
def subt(a, b):
    return (a - b)
def multi(a, b):
    return (a * b)
def divs(a, b):
    return (a / b)
def mod(a, b):
    return (a % b)


print("Addition", add(5, 2))
print("Subtraction", subt(5, 2))
print("Mutliplication", multi(5, 2))
print("Division", divs(5, 2))
print("type of divistion", type(divs(5, 2)))
print("Modular", mod(5, 2))
